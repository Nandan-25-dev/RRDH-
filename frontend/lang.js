/* ===== Language State ===== */
let currentLang = localStorage.getItem('rdch-lang') || 'en';

/* ===== Toggle Language ===== */
function toggleLanguage() {
  currentLang = currentLang === 'en' ? 'kn' : 'en';
  localStorage.setItem('rdch-lang', currentLang);
  updateTexts();
}

/* ===== Apply All Translations ===== */
function updateTexts() {
  const t = translations[currentLang];
  if (!t) return;

  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (t[key] !== undefined) {
      // Replace \n with <br> for multiline
      el.innerHTML = t[key].replace(/\n/g, '<br>');
    }
  });

  /* Update language button text */
  document.querySelectorAll('#lang-toggle').forEach(btn => {
    btn.textContent = currentLang === 'en' ? 'EN / ಕನ್ನಡ' : 'ಕನ್ನಡ / EN';
  });

  /* Set html lang attribute */
  document.documentElement.lang = currentLang === 'kn' ? 'kn' : 'en';
}

/* ===== Initialise ===== */
function initLang() {
  updateTexts();
  document.querySelectorAll('#lang-toggle').forEach(btn => {
    btn.addEventListener('click', (e) => {
      e.preventDefault();
      toggleLanguage();
    });
  });
}

if (document.readyState === 'loading') {
  document.addEventListener('DOMContentLoaded', initLang);
} else {
  initLang();
}
